public class Main {

    public static void main(String[] args) {
        //For Dongusu
        for (int i=0;i<10;i++){
            System.out.println(i);
        }
        System.out.println("For Döngüsü Bitti.");
        //While Dongusu
        int i=0;
        while(i<10){
            System.out.println(i);
            i++;
        }
        System.out.println("While Döngüsü Bitti.");
        //Do-While Dongusu
        int j=44;
        do {
            System.out.println(j);
            j++;
        }while(j<10);
        System.out.println("Do-While Döngüsü Bitti.");
    }
}
